/* Phase 2b of the providers-from-handler-call pipeline. Emits a single
 * generated module that maps Elysia route patterns (from
 * `scanAngularHandlerCalls` results) to their derived `APP_BASE_HREF`
 * base paths. The SSR handler imports it at module-load time and
 * derives the right base for each incoming request — so users never
 * have to write `{ provide: APP_BASE_HREF, useValue: "/portal/" }` by
 * hand.
 *
 * Same source of truth the client-side providers file uses
 * (`emitAngularProvidersFiles`), so both phases see the same base path
 * for any given page. Hydration is consistent without the page module
 * being involved at all. */

import { mkdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { getFrameworkGeneratedDir } from '../utils/generatedDir';
import type { AngularHandlerCall } from './scanAngularHandlerCalls';

const deriveBasePath = (mountPath: string | null): string | null => {
	if (!mountPath) return null;
	if (!mountPath.endsWith('/*')) return null;

	const trimmed = mountPath.slice(0, -1);

	return trimmed === '/' ? null : trimmed;
};

const escapeForRegex = (literal: string) =>
	literal.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

/** Convert a mount path like `/portal/` into a regex source string that
 *  matches request URLs prefixed by it. `/portal/` → `^/portal(/|$)`. */
const mountToPatternSource = (basePath: string) => {
	// Strip the trailing slash so the regex matches both `/portal` (exact)
	// and `/portal/...` (descendants).
	const withoutTrailing = basePath.replace(/\/$/, '');

	return `^${escapeForRegex(withoutTrailing)}(\\/|$)`;
};

export const getRouteMountsOutputPath = (projectRoot: string) =>
	join(
		getFrameworkGeneratedDir('angular', projectRoot),
		'route-mounts.ts'
	);

export const emitAngularRouteMounts = (
	projectRoot: string,
	calls: AngularHandlerCall[]
): string => {
	const entries: Array<{ patternSource: string; basePath: string }> = [];
	const seen = new Set<string>();
	for (const call of calls) {
		const basePath = deriveBasePath(call.mountPath);
		if (!basePath) continue;
		if (seen.has(basePath)) continue;
		seen.add(basePath);
		entries.push({
			basePath,
			patternSource: mountToPatternSource(basePath)
		});
	}

	const outputPath = getRouteMountsOutputPath(projectRoot);
	mkdirSync(getFrameworkGeneratedDir('angular', projectRoot), {
		recursive: true
	});

	const literal =
		entries.length === 0
			? '[]'
			: `[\n${entries
					.map(
						(e) =>
							`\t{ pattern: new RegExp(${JSON.stringify(
								e.patternSource
							)}), basePath: ${JSON.stringify(e.basePath)} }`
					)
					.join(',\n')}\n]`;

	const body = `/* AUTOGENERATED by AbsoluteJS — see \`emitAngularRouteMounts\`. */
/* eslint-disable */
export type AngularRouteMount = { pattern: RegExp; basePath: string };
export const routeMounts: AngularRouteMount[] = ${literal};
`;

	writeFileSync(outputPath, body, 'utf-8');

	return outputPath;
};
